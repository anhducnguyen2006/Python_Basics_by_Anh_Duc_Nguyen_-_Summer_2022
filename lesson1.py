# Funkcja print()
print("Hello World")

# Typy Danych
liczba_całkowita = 1 # int
liczba_zmiennoprzecinkowa = 50.7 # float
napis = "Hello World" # string (str)
typ_logiczny = True #bool

print(liczba_całkowita) # wydrukuje się 1
print(liczba_zmiennoprzecinkowa) #wydrukuje się 50.7
print(napis) #wydrukuje się "Hello World"
print(typ_logiczny) #wydrukuje się True

print(type(liczba_całkowita)) #wydrukuje się <class 'int'>
print(type(liczba_zmiennoprzecinkowa)) #wydrukuje się <class 'float'>
print(type(napis)) #wydrukuje się <class 'int'>
print(type(typ_logiczny)) #wydrukuje się <class 'bool'>


